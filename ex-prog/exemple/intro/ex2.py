###################### Exemple variabile ######################

# Declarare & Initializare variabile
my_number = 3           # nr intreg
my_real_number = 3.3    # nr real
my_string = "Salvee"    # string 
my_bool = True          # boolean
my_magic = None         # variabila goala

# Afisare variabile cu formatare
print("Numarul meu intreg: {}".format(my_number))
print("Numarul meu real: {}".format(my_real_number))
print("Stringul meu: {}".format(my_string))
print("Booleanul meu: {}".format(my_bool))
print("Magiaa: {}".format(my_magic))

