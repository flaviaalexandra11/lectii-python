###################### Exemple if-elif-else ######################

x = 8
if x < 10:
    print("Mai mic ca 10.")

x = 10
if x < 10:
    print("Mai mic ca 10.")
else:
    print("Mai mare sau egal cu 10.")

x = 10
if x < 10:
    print("Mai mic ca 10.")
elif x == 0:
    print("Egal cu 10.")
else:
    print("Mai mare ca 10.")
