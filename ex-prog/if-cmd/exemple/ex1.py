# schimbati valoarea lui x si observati ce se intampla
x = 5

# if simplu
if x == 2:
    print("Egal!")

print("---------")

# if-else
if x == 2:
    print("Egal!")
else:
    print("Diferit!")

print("---------")

# if-elif-else
if x == 2: 
    print("Egal!")
elif x < 2: 
    print("Mai mic!")
else:
    print("Mai mare!")
